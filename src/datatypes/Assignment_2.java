package datatypes;

public class Assignment_2 extends Arrays {
	public static void main(String[] args) {
		
		//decrement operator
		
		System.out.println("for statement example with decrement operator:");
		for(int j=10;j>7;j--)
		{
			System.out.println(j);
		}
		
		//increment by 2
		System.out.println("for statement example increasing value by 2:");
		for(int j=10;j<15;j++)
		{
			int NewValue=j+2;
			System.out.println(NewValue);
			j++;
		}
		
		
		
	}

}
  